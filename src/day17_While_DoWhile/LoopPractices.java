package day17_While_DoWhile;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);//123
        }

        System.out.println("-----------------------------------");

        int j=1;
        while(j <= 10){

            System.out.println(j);
            j++;
        }

        System.out.println("-----------------------------------");
        int num1 =1;  //
        do{

            System.out.println(num1); //
            num1++; //
        }while(num1 <= 10); //





    }

}