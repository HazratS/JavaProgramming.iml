package Day03_EscapeSequences;
/*
Escape sequences: MUST be given with double quotes
\n: starts a new line
\t: tab: paragraph space (tab)
\\: single back slash (\) (if you want one (\)to be printed out, you need to give two back slashed(\\)
\": double quote (in order to print double quote ("), we need use (\").

 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("-----------------------------------------");

        System.out.println("Hello Cydeo? \nHow are you all today? \nIt is nice to see you all. \nWhat class do we have next week?");

        System.out.println("----------------------------------------------------");
        System.out.println("\tJava is Cool Programming Language");

        System.out.println("-----------------------------------------------------");

        System.out.println("/ \\");

        System.out.println("My favorite TV show is \"Game of Throne\".");

    }


}
